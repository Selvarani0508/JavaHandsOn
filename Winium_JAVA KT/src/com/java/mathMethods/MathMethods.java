package com.java.mathMethods;

public class MathMethods {
	
	public static void main(String[] args) {  
		 
		  
		System.out.println(Math.sqrt(81)); //Square root 
		
		System.out.println(Math.pow(2,3)); //Power
		
		System.out.println(Math.min(4,6)); //Minimum Value
		
		System.out.println(Math.max(4,6)); //Maximum value
		
		System.out.println(Math.round(4.3)); //Round value
		System.out.println(Math.round(4.6));
		
		System.out.println(Math.floor(4.3)); //Rounding down value
		System.out.println(Math.floor(4.6));
		
		System.out.println(Math.ceil(4.3)); //Rounding up value
		System.out.println(Math.ceil(4.6));
		
		System.out.println(Math.abs(-5));  //Absolue value
		System.out.println(Math.round(6));
		
		System.out.println(Math.random()); //Random number
		
		
		System.out.println(Math.PI); //PI value


}
}
